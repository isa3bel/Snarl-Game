package src;

/**
 * A connector between two towns in the RPG.
 */
public class Road {

  private String from;
  private String to;

  /**
   * Gets the from town
   * @return the from town
   */
  public String getFrom() {
    return this.from;
  }

  /**
   * Gets the to town
   * @return the to town
   */
  public String getTo() {
    return this.to;
  }
}
